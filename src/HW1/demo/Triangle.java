package HW1.demo;

public class Triangle {
    private double l,w;

    public Triangle() {
    }

    public Triangle(double l, double w) {
        this.l = l;
        this.w = w;
    }



    public double getL() {
        return l;
    }

    public void setL(double l) {
        this.l = l;
    }

    public double getW() {
        return w;
    }

    public void setW(double w) {
        this.w = w;
    }
}
